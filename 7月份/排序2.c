#include <string.h>
#include <stdio.h>
int main(void)
{
/*--------------------------------------------------------*/
	const int a[6] = {6,2,1,5,4,3};//设无规律的只读数值
	int n, m, t;
/*--------------------------------------------------------*/
	for(n = 0; n < 5; n++)
		for(m = n + 1; m < 6; m++)
			if(strcmp(&a[n], &a[m]) > 0)
			{
				t = a[n];
				a[n] = a[m];
				a[m] = t;
			}
/*--------------------------------------------------------*/
	for(n = 0; n < 6; n++)
		printf("%d ", a[n]);
/*--------------------------------------------------------*/
	return 0;
}
